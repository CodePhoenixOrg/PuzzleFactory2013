<ul>	
	<p lang='fr'><b>1 - Qu'est-ce que le projet iPuzzle ?</b></p>
	<p lang='fr'>
	Le but principal de iPuzzle est de proposer aux entreprises une nouvelle façon 
de gérer leur resources en environnement intranet ou extranet. Cette "nouvelle" approche
consiste à remplacer l'usage d'un navigateur internet (IE, Mozilla, Safari, etc) par
une application Windows conventionnelle qui est spécialement conçue pour l'utilisation
en environnement intranet ou extranet.</p>     
	<p lang='fr'>
	Cependant, le développement d'applications web utilisant un navigateur reste dans le
cadre du projet iPuzzle.<p>
	<p lang='fr'>
	La première approche que propse iPuzzle a plusieurs avantages. La technologie cliente
étant unique, on ne fait plus de savant mélange entre HTML, Java, JavaScript, XML, ASP ou PHP.
Le développement des applications devient plus rapide, réduit le nombre de bogues et de fait
accroit la sécurité.<p>
	<p lang='fr'>
	iPuzzle est un projet qui regroupe plusieurs technologies communément considérées
comme concurrentes. La partie cliente du projet utilise <a href="http://www.microsoft.com/net/">
Microsoft .Net</a> et globalement la plate-forme Windows. La partie serveur utilise essentiellement
des technologies à source ouvert (<a href="http://www.opensource.org">Open Source</a>) à travers 
PHP, Apache et Linux. 
	<p lang='fr'>
	<b>2 - Le coeur de iPuzzle</b></p>
	<p lang='fr'>
	iPuzzle est mis en oeuvre à travers deux séries de composants, l'une côté client appelée
WinPieces, l'autre côté serveur appelée WebPieces. Ces séries sont en cours de développement mais
doivent à terme proposer les même services, que leur utilisation soit sous Windows ou dans un
navigateur.</p>
</ul>
