	<ul>iPuzzle requiert l'emploi des outils et technologies suivantes :</ul>
	
	<ul>	<a name="delphi">C# & Delphi .Net en tant que langauges et environnement RAD principaux :<br></a>
		<ul>Facile à appréhender comme Visual Basic et puissant comme C++,<br>
			Delphi est idéal pour développer des applications complexes rapidement,<br>
			et agrémentées de contrôles qui donne un aspect moderne.<br>
			Toutefois, mon choix s'est porté sur C# pour l'écriture des WinPieces<br>
			afin de rester indépendant de Delphi, lequel s'accompagne de quelques<br>
			fichiers assez lourds.
		</ul>
	</ul>
	<ul>	<a name="apache">Apache en tant que serveur web :<br></a>
		<ul>	Relativement simple à administrer, Apache est aussi rapide<br>
			et modulaire. Il est présent sur de nombreuses plate-formes.<br>
			C'est aussi la pierre angulaire du trio Apache/PHP/MySQL<br>
			très apprécié des webmasters et herbergeurs de sites web<br>
			dynamiques.
		</ul>
	</ul>
	<ul>	<a name="php">PHP pour les scripts côté serveur :<br></a>
		<ul>	Rapide et puissant car large de compétences, le langage PHP<br>
			est choix évident pour les scripts côté serveur. Sa ressemblance<br>
			avec PERL le rend facile d'accès et sa modularité autorise l'ajout<br>
			de compétences tout en étant indépendant de la plate-forme.
		</ul>
	</ul>
	<ul>	<a name="mysql">MySQL en tant que SGBDR principal :<br></a>
		<ul>	Si MySQL est encore incomplet (pas de procédures stockées, etc.),<br>
			il faut lui reconnaître certaines qualités, à savoir sa rapidité et<br>
			sa grande popularité auprès des webmasters, et des herbergeurs.<br>
			De plus, l'accès aux bases de données MySQL est optimisé grâce<br>
			à son intégration dans le moteur de PHP4. MySQL est égalment<br>
			présent sur de nombreuses plate-formes.
		</ul>
	</ul>

